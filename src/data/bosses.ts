  /** Display name. */
  name: string;
  /** One-line hook. */
  tagline: string;
  /** 1–2 sentence description. */
  description: string;
  /** Hero image (path under /public). */
  heroImage: string;
  gallery?: string[];
  color: {
    primary: string;
    secondary: string;
    accent: string;
  };
  tier: BossTier;
  /** Game-side faction token from the boss registry. */
